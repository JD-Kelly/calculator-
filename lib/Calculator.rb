class Calculator 
    def add(number_one, number_two)
        "The answer is #{number_one + number_two}"
    end 

    def subtract(number_one, number_two)
        "The answer is #{number_one - number_two}"
    end 

    def multiply(number_one, number_two)
        "The answer is #{number_one * number_two}"
    end 
end 